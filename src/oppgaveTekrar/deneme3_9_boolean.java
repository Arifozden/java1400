package oppgaveTekrar;

public class deneme3_9_boolean {
    public static void main(String[] args) {
        int tall1 = 5;
        int tall2 = 4;
        boolean a1=tall1 == tall2;
        boolean a2=tall1 != tall2;
        boolean a3=tall1 <= tall2;
        boolean a4=tall1 >= tall2;
        boolean a5=tall1 < tall2;
        boolean a6=tall1 > tall2;
        System.out.println(a1);
        System.out.println(a2);
        System.out.println(a3);
        System.out.println(a4);
        System.out.println(a5);
        System.out.println(a6);
    }
}
