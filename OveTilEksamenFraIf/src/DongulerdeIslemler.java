public class DongulerdeIslemler {
    public static void main(String[] args) {
        int antall = 0;
        antall = antall + 1; // øker antall med 1
        System.out.println("Antall = "+antall);
        antall +=1; // samme som over
        System.out.println("Antall = "+antall);
        antall ++;  // samme som over
        System.out.println("Antall = "+antall);
        antall = antall - 1; // minsker antall med 1
        System.out.println("Antall = "+antall);
        antall -=1; // samme som over
        System.out.println("Antall = "+antall);
        antall --;  // samme som over
        System.out.println("Antall = "+antall);
        antall = antall + 2; // øker antall med 2
        System.out.println("Antall = "+antall);
        antall +=2; // samme som over
        System.out.println("Antall = "+antall);
        antall = antall * 2; // ganger antall med 2
        System.out.println("Antall = "+antall);
        antall *=2; // samme som over
        System.out.println("Antall = "+antall);
        antall = antall / 2; // ganger antall med 2
        System.out.println("Antall = "+antall);
        antall /=2; // samme som over
        System.out.println("Antall = "+antall);
    }
}
